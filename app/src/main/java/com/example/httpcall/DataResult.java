package com.example.httpcall;

public class DataResult {

    String  Id;
    String NameAR;
    String NameEN;

    public void setId(String id) {
        Id = id;
    }

    public void setNameAR(String nameAR) {
        NameAR = nameAR;
    }

    public void setNameEN(String nameEN) {
        NameEN = nameEN;
    }

    public String getId() {
        return Id;
    }

    public String getNameAR() {
        return NameAR;
    }

    public String getNameEN() {
        return NameEN;
    }
}
